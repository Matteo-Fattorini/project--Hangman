  //function that pick a random word from the words array
  function randomPick(wordList) {
    var word = wordList[Math.floor(Math.random() * wordList.length)];
    while (word.length < 5) {
      word = wordList[Math.floor(Math.random() * wordList.length)];
    }
    return word;
  }

  //function that creates letter for the word length
  function createLetters(word) {
    for (var i = 0; i < word.length; i++) {
      var createdLetter = document.createElement("div");
      createdLetter.className = "letter noDisplay";
      $(createdLetter).html(word[i]);
      $(".computer-word").append(createdLetter);
    }
  }

  //function for gameOver
  function gameOver() {
    document.querySelectorAll(".letter").forEach((e) => e.remove()); //removes all old letters from DOM
    var word = randomPick(words); // new word generated
    createLetters(word);
    console.log(word);
    game = true;
    $("#solution-btn").text("Soluzione");
    $("#score-text").css("color", "white"); //bring everything to vanilla state
    tries = 8;
    $("#round-level").html(tries);
  }

  var tries = 8; //max tries
  var game = true; //game is defaulted at true

  //click of the start button
  $("#startGame").click(function () {
    var word = randomPick(words);
    createLetters(word); //generates a new word, creates letters and hide instructions
    console.log(word);
    $(".game-container").show();
    $("#instructions").hide();
  });

  //click on solution button. Will check for identity of user and solution
  $("#solution-btn").click(function () {
    if (tries <= 0) {
      game = false;
    }
    var counter = 0;
    var userData = $("#user-pick").val();
    var letters = $(".letter").text();
    if (!game) {
      //if game is false begin a new game
      gameOver();
    } else if (userData.length == 0) {
      alert("Non hai inserito niente");
    } else {
      for (i = 0; i < userData.length; i++) {
        if (userData[i] == letters[i]) {
          ++counter;
        }
      }
      if ($(".letter").length == counter) {
        //solution check. Every time a letter is found, counters goes up. If counter == secretword length
        $("#solution-btn").text("Nuova Partita"); //game is won
        $("#score-text").css("color", "green");
        $(".letter").each(function () {
          $(this).removeClass("noDisplay");
          $(this).css("color", "green");
          game = false;
        });
      } else {
        --tries;
        $("#round-level").html(tries); //if solution is not correct tries goes down
      }
      if ($(".letter").length != counter && tries == 0) {
        alert("hai perso"); //if solution not found and tries are zero, gameOver sequence
        gameOver();
      }
      $("#user-pick").val("");
    }
  });

  $("#singleWord-btn").click(function () {
    if (tries <= 0) {
      game = false;
    }
    if (!game) {
      //check if game is lost, if so begin a new one
      gameOver();
    }
    var userData = $("#user-pick").val();
    var letters = $(".letter");
    if (userData.length > 1 || userData.length == 0) {
      alert("Hai inserito più di una lettera");
    } else {
      for (var i = 0; i < letters.length; i++) {
        //check if the letter is present in the secret letters
        if (letters[i].innerHTML === userData) {
          //if so, show the corrisponding letter
          var currentLetter = $(".letter");
          var currentLetterIndex = currentLetter[i];
          $(currentLetterIndex).removeClass("noDisplay");
        }
      }
      tries--;
      $("#user-pick").val("");
      $("#round-level").html(tries); //if tries goes to zero  is game over
      if (tries == 0) {
        alert("Hai perso :( ");
        gameOver();
      }
    }
  });
